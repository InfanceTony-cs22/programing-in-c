#include <stdio.h>
#include <string.h>

// Define a structure for a Person
struct Person {
    char name[50];
    int age;
    float height;
};

int main() {
    // Create instances of the Person structure
    struct Person person1;
    struct Person person2;

    // Initialize the data for person1
    strcpy(person1.name, "Alice");
    person1.age = 30;
    person1.height = 5.7;

    // Initialize the data for person2
    strcpy(person2.name, "Bob");
    person2.age = 25;
    person2.height = 6.0;

    // Display the information for person1
    printf("Person 1:\n");
    printf("Name: %s\n", person1.name);
    printf("Age: %d\n", person1.age);
    printf("Height: %.2f feet\n", person1.height);

    // Display the information for person2
    printf("\nPerson 2:\n");
    printf("Name: %s\n", person2.name);
    printf("Age: %d\n", person2.age);
    printf("Height: %.2f feet\n", person2.height);

    return 0;
}

